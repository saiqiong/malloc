#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*n;
	char	*o;

	if (!(n = (char *)malloc(ft_strlen(src) + 1)))
		return (NULL);
	o = n;
	while (*src)
		*n++ = *src++;
	*n = '\0';
	return (o);
}
